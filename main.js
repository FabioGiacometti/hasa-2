let simpleSlide = (imageRoute) =>
  `<div class="swiper-slide"><img src='${imageRoute}'></div>`;

let titledSlide = (data) =>
  `<div class="swiper-slide"><h1 class="title"> ${data.name}</h1><img src='${data.image}'></div>`;

